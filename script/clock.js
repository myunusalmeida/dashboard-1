function startTime() {
  var today = new Date();
  var h = today.getHours();
  var m = today.getMinutes();

  var tahun = today.getFullYear();
  var bulan = today.getMonth();
  var tanggal = today.getDate();
  switch (bulan) {
    case 0:
      bulan = "Januari";
      break;
    case 1:
      bulan = "Februari";
      break;
    case 2:
      bulan = "Maret";
      break;
    case 3:
      bulan = "April";
      break;
    case 4:
      bulan = "Mei";
      break;
    case 5:
      bulan = "Juni";
      break;
    case 6:
      bulan = "Juli";
      break;
    case 7:
      bulan = "Agustus";
      break;
    case 8:
      bulan = "September";
      break;
    case 9:
      bulan = "Oktober";
      break;
    case 10:
      bulan = "November";
      break;
    case 11:
      bulan = "Desember";
      break;
  }
  document.getElementById("clock").innerHTML = h + ":" + m;
  document.getElementById("days").innerHTML =
    tanggal + " " + bulan + " " + tahun;
}
function checkTime(i) {
  if (i < 10) {
    i = "0" + i;
  } // add zero in front of numbers < 10
  return i;
}
